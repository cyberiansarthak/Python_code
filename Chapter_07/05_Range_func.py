#RANGE FUNCTION IS USED TO GENERATE A SEQUENCE OF NUMBERS
for i in range(10):
    print(i)
print("\n")    
#WE CAN ALSO SPECIFY THE START,STOP AND STEP-SIZE

#START
for i in range(1,50): # IT PRINT THE NUMBER FROM 1-49 WHICH IS ONE LESS THEN 49
    print(i) 
print("\n")  

#STEP SIZE
#PRINT TABLE OF 5
for i in range(5,55,5):
    print(i)
print("\n")  

#BY DEFAULT STEP-SIZE IS ONE
for i in range(1,10,1):
    print(i)

    
