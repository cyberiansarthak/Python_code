#there are primarily followings datatype which is used in python
integers = 1456 # integer data type
Floating_numbers = 45.32 # float data type
String = ''' i am the only "genius"''' # String data type
Boolean = True  # boolean data type (it is either True or False)
none = None  # this data type used to print nothing on the terminal 

#printing the above variables
print(integers)
print(Floating_numbers)
print(String)
print(Boolean)
print(none)

#printing the data type of the variable
#type is built in function which is used to print the data type of the variable
print(type(integers))
print(type(Floating_numbers))
print(type(String))
print(type(Boolean))
print(type(none))


