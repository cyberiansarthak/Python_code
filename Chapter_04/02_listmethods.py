l1=[1,7,69,36,24]

#SLICING
print(l1[1:4:2])

#SORT THE LIST
l1.sort()
print(l1)

#REVERSE THE LIST
l1.reverse()
print(l1)

#APPEND TO THE LIST
l1.append(25)
print(l1) # add the element at the end

#INSERT TO THE LIST
l1.insert(4,78)
print(l1)# add the element on index no. 4

#POP FROM LIST
l1.pop(4)
print(l1)#remove the element on the index no. 4

#REMOVE THE ELEMENT FROM LIST
l1.remove(1)#remove the element "1"
print(l1)